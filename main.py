def on_key_down(key):
    global player_vy, touching_ground, is_moving, current_pframe, game_state
   
    if touching_ground is True:
        if key == keys.W and touching_ground:
            player_vy = -10  
            if sound_on:
                sounds.jump.play()
            touching_ground = False  
        if key == keys.A:
            current_pframe = 4
        if key == keys.D:
            current_pframe = 0
        if key == keys.ESCAPE and game_state == "game":
            game_state = "menu"

def on_mouse_down(pos):
    global game_state, sound_on
    if game_state == "menu":
        if sound_on:
            sounds.select.play()
        if button_play.collidepoint(pos):
            game_state = "game"
        if button_sound.collidepoint(pos):
            sound_on = not sound_on
            if sound_on:
                music.set_volume(0.5)
            else:
                music.set_volume(0.0)
        if button_exit.collidepoint(pos):
            exit()

def update():
    global current_pframe, frame_time, is_moving, standby_limit, moving_limit, standby_base, player_vy, touching_ground
    if game_state != "game":
        return
    player.y += player_vy
    player_vy += gravity

    if check_collision():
        player_vy = 0
        touching_ground = True
    
    
    for enemy in enemies:
        
        if enemy.is_moving:
            enemy.x += enemy.direction * enemy.inverter

            if enemy.direction == 1:
                enemy.start = 4
                enemy.end = 5
                #direita
            if enemy.direction == -1:
                enemy.start = 2
                enemy.end = 3
                #esquerda

            if enemy.x > enemy.limits[1] or enemy.x < enemy.limits[0]:
                enemy.is_moving = False
                enemy.inverter = 0
                
        else:
            if enemy.direction == 1:
                enemy.start = 0
                enemy.end = 1
            if enemy.direction == -1:
                enemy.start = 6
                enemy.end = 7
            enemy.timer += 1
            if enemy.timer > 20:
                enemy.direction *= -1
                enemy.inverter = 1
                enemy.is_moving =True
                enemy.timer = 0
                    
    if keyboard.A: 
        player.x -= 4 
        standby_limit = 5
        moving_limit = 7

        standby_base = 4 
    if keyboard.D:  
        player.x += 4
        standby_limit = 1
        moving_limit = 3
        standby_base = 0
        
    camera_offset[0] = player.x - WIDTH // 2
    camera_offset[1] = player.y - HEIGHT // 2

    for enemy in enemies:  
        enemy.frame_timer += 1
        if enemy.is_moving:
            if enemy.frame_timer > 10:
                enemy.frame_timer = 0
                enemy.frame_index += 1
                
                
                if enemy.frame_index > enemy.end:
                    enemy.frame_index = enemy.start

                enemy.image = enemy.frames[enemy.frame_index]
                
    is_moving = keyboard.A or keyboard.D

    if is_moving is True:
        if frame_time >= 10: 
            current_pframe += 1

            if current_pframe > moving_limit:
                current_pframe = standby_limit

            player.image = pframes[current_pframe]  
            frame_time = 0  
    else:
        if frame_time >= 10:  
            current_pframe += 1 

            if current_pframe >= standby_limit:
                current_pframe = standby_base
            player.image = pframes[current_pframe] 
            frame_time = 0 

    for enemy in enemies:
        if player.colliderect(enemy):
            player_die()
    if player.y > HEIGHT:
        player_die()


def check_collision():
    player_rect = get_player_rect()
    for bloco in colisores:
        if player_rect.colliderect(bloco):
            player.y = bloco.top - 16
            return True
    return False
